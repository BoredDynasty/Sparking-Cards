How to make the tooltip work:


Put the tooltipGui in StarterGui
And to make the tooltip appear when hovered on a part, copy the values in this script into the part you want the tool tip to show up.
If you want to disable the tooltip for this part, set the "Tooltip" value to false
Change the "Name" and "Desc" Values to the text you want to show up.

You can also modify the tooltip ui. You just need a textlabel named "NameUI" and "DescUI"



